package com.rootar.metier;

public class Avoir {

    private int idTheme;
    private int idPays;

    public Avoir() {
        this.idTheme = idTheme;
        this.idPays = idPays;
    }

    public int getIdTheme() {
        return idTheme;
    }

    public void setIdTheme(int idTheme) {
        this.idTheme = idTheme;
    }

    public int getIdPays() {
        return idPays;
    }

    public void setIdPays(int idPays) {
        this.idPays = idPays;
    }
}
