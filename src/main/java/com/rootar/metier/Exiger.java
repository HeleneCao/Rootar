package com.rootar.metier;

public class Exiger {

    private int idPays;

    private int idSante;

    public Exiger() {
        this.idPays = idPays;
        this.idSante = idSante;
    }

    public int getIdPays() {
        return idPays;
    }

    public void setIdPays(int idPays) {
        this.idPays = idPays;
    }

    public int getIdSante() {
        return idSante;
    }

    public void setIdSante(int idSante) {
        this.idSante = idSante;
    }
}
